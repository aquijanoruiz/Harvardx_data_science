#Central limit theorem
#The Central Limit Theorem (CLT) tells us that when the number of draws, also called the sample size, is large, 
#the probability distribution of the sum of the independent draws is approximately normal. Because sampling models 
#are used for so many data generation processes, the CLT is considered one of the most important mathematical insights in history.

#The central limit theorem works when the number of draws is large. But large is a relative term.
#How big is large? 15, 100, a million? In many circumstances, as few as 30 draws is enough to make 
#the CLT useful. In specific instances, as few as 10 is enough. #However, these should not be considered 
#general rules.

#Note for example, that when the probability of success is very small, we need larger sample sizes.
#Consider for example, the lottery. In these cases, the Poisson distribution is more appropriate.

